package com.example.onlinejipmerhospitalappointments;

public class DeptName {
    String name, ph1, ph2;

    public DeptName(String name, String ph1, String ph2) {
        this.name = name;
        this.ph1 = ph1;
        this.ph2 = ph2;
    }

    public String getName() {
        return name;
    }

    public String getph1() {
        return ph1;
    }

    public String getph2() {
        return ph2;
    }
}